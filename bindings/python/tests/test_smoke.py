def test_accuracy_measures():
    fx = FCFX["accuracy_small"]
    a, f = np.array(fx["actual"]), np.array(fx["forecast"])
    ins = np.array(fx["insample_for_mase"])
    r = tsecon.accuracy(a, f, insample=ins, period=1)
