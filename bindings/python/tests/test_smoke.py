def test_arima_fit_beats_statsmodels_on_nile():
    fx = json.loads((FIXTURES / "arima.json").read_text())["nile_arma11c_fit"]
    r = tsecon.arima_fit(NILE, p=1, d=0, q=1, constant=True, forecast_steps=12)
